// ALX: derived from loom.loom §"emit_typescript" and language-spec.md §9
// TypeDef → export interface + Zod schema
// EnumDef → export enum
// FnDef → export function with JSDoc
// BeingDef → export class with TS equivalents of Rust methods
// EcosystemDef → export namespace with signal types

use crate::ast::*;

/// G3: TypeScriptEmitter struct — tests call `TypeScriptEmitter::new().emit(&module)`.
pub struct TypeScriptEmitter;

impl TypeScriptEmitter {
    pub fn new() -> Self { TypeScriptEmitter }
    pub fn emit(&self, module: &Module) -> String {
        emit_typescript(module)
    }
}

pub fn emit_typescript(module: &Module) -> String {
    let mut out = String::new();

    // Header
    out.push_str("// Generated by Loom ALX compiler.\n");
    out.push_str("// @ts-nocheck\n\n");
    out.push_str("import { z } from 'zod';\n");

    // ES imports
    for imp in &module.imports {
        let mod_path: String = imp.chars().enumerate().map(|(i, c)| {
            if c.is_uppercase() && i > 0 { format!("-{}", c.to_lowercase()) }
            else { c.to_lowercase().to_string() }
        }).collect();
        out.push_str(&format!("import * as {} from \"./{}\";\n", imp, mod_path));
    }
    if !module.imports.is_empty() { out.push('\n'); }

    // Module describe as JSDoc
    if let Some(desc) = &module.describe {
        out.push_str(&format!("/** {} */\n", desc));
    }
    for ann in &module.annotations {
        out.push_str(&format!("/** @{}: {} */\n", ann.key, ann.value));
    }

    // Flow labels → branded types
    for fl in &module.flow_labels {
        for ty_name in &fl.types {
            out.push_str(&format!(
                "export type {} = string & {{ readonly _sensitivity: \"{}\" }};\n",
                ty_name, fl.label
            ));
        }
    }
    if !module.flow_labels.is_empty() { out.push('\n'); }

    // Unit-branded types for Float<unit>
    let mut unit_types: std::collections::HashSet<String> = std::collections::HashSet::new();
    crate::codegen::rust::collect_unit_types(module, &mut unit_types);
    for unit in &unit_types {
        let branded_name = capitalize(unit);
        out.push_str(&format!(
            "export type {} = number & {{ readonly _unit: \"{}\" }};\n",
            branded_name, unit
        ));
    }
    if !unit_types.is_empty() { out.push('\n'); }

    // Lifecycle → state union type + generic interface
    for lc in &module.lifecycle_defs {
        out.push_str(&format!(
            "export type {}State = {};\n",
            lc.type_name,
            lc.states.iter().map(|s| format!("\"{}\"", s)).collect::<Vec<_>>().join(" | ")
        ));
        out.push_str(&format!(
            "export interface {}<State extends {}State> {{ readonly _state: State; }}\n",
            lc.type_name, lc.type_name
        ));
    }
    if !module.lifecycle_defs.is_empty() { out.push('\n'); }

    // Wrap items in namespace
    let ns = &module.name;
    out.push_str(&format!("export namespace {} {{\n", ns));

    // Interface defs → export interface
    for iface in &module.interface_defs {
        emit_ts_interface_def(iface, &mut out);
    }

    // Items
    for item in &module.items {
        match item {
            Item::Type(t) => emit_ts_type(t, &mut out),
            Item::Enum(e) => emit_ts_enum(e, &mut out),
            Item::RefinedType(r) => emit_ts_refined(r, &mut out),
            Item::Fn(f) => emit_ts_fn(f, &mut out),
        }
    }

    // Implements → export class
    if !module.implements.is_empty() {
        let impl_name = format!("{}Impl", module.name);
        let trait_names = module.implements.join(", ");
        out.push_str(&format!("  export class {} implements {} {{\n", impl_name, trait_names));
        for item in &module.items {
            if let Item::Fn(f) = item {
                emit_ts_fn_method(f, &mut out);
            }
        }
        out.push_str("  }\n\n");
    }

    // Beings → export class
    for being in &module.being_defs {
        emit_ts_being(being, &mut out);
    }

    // Ecosystems → export namespace
    for eco in &module.ecosystem_defs {
        emit_ts_ecosystem(eco, &mut out);
    }

    out.push_str("}\n");
    out
}

fn emit_ts_type(t: &TypeDef, out: &mut String) {
    out.push_str(&format!("  export interface {} {{\n", t.name));
    for field in &t.fields {
        let ts_ty = type_to_ts(&field.ty);
        for ann in &field.annotations {
            out.push_str(&format!("    /** @{} */\n", ann.key));
        }
        out.push_str(&format!("    {}: {};\n", field.name, ts_ty));
    }
    out.push_str("  }\n\n");

    // Zod schema
    out.push_str(&format!("  export const {}Schema = z.object({{\n", t.name));
    for field in &t.fields {
        let zod = type_to_zod(&field.ty);
        out.push_str(&format!("    {}: {},\n", field.name, zod));
    }
    out.push_str("  });\n\n");
}

fn emit_ts_enum(e: &EnumDef, out: &mut String) {
    let all_unit = e.variants.iter().all(|v| v.payload.is_none());
    if all_unit {
        // Union type for simple enums
        let variants: Vec<String> = e.variants.iter()
            .map(|v| format!("\"{}\"", v.name))
            .collect();
        out.push_str(&format!("  export type {} = {};\n\n", e.name, variants.join(" | ")));
    } else {
        // Tagged union for mixed enums
        let variants: Vec<String> = e.variants.iter().map(|v| {
            match &v.payload {
                None => format!("  {{ tag: \"{}\"; }}", v.name),
                Some(ty) => format!("  {{ tag: \"{}\"; value: {}; }}", v.name, type_to_ts(ty)),
            }
        }).collect();
        out.push_str(&format!("  export type {} =\n{}\n\n", e.name, variants.join("\n  | ")));
    }
}

fn emit_ts_refined(r: &RefinedType, out: &mut String) {
    let base = type_to_ts(&r.base_type);
    out.push_str(&format!("  // Refined type: {}\n", r.name));
    out.push_str(&format!("  // predicate: {}\n", r.predicate));
    out.push_str(&format!("  export type {} = {};\n\n", r.name, base));
}

fn emit_ts_interface_def(iface: &InterfaceDef, out: &mut String) {
    out.push_str(&format!("  export interface {} {{\n", iface.name));
    for method in &iface.methods {
        let params: Vec<String> = method.type_sig.params.iter().enumerate()
            .map(|(i, ty)| format!("p{}: {}", i, type_to_ts(ty)))
            .collect();
        let ret = type_to_ts(&method.type_sig.return_type);
        out.push_str(&format!("    {}({}): {};\n", method.name, params.join(", "), ret));
    }
    out.push_str("  }\n\n");
}

fn emit_ts_fn(f: &FnDef, out: &mut String) {
    if let Some(desc) = &f.describe {
        out.push_str(&format!("  /** {} */\n", desc));
    }
    for ann in &f.annotations {
        match ann.key.as_str() {
            "idempotent" => out.push_str("  /** @idempotent — safe to retry; f(f(x)) = f(x) */\n"),
            "commutative" => out.push_str("  /** @commutative — argument order does not matter */\n"),
            _ => out.push_str(&format!("  /** @{} */\n", ann.key)),
        }
    }
    let params: Vec<String> = f.type_sig.params.iter().enumerate()
        .map(|(i, ty)| format!("p{}: {}", i, type_to_ts(ty)))
        .collect();
    let is_async = matches!(&f.type_sig.return_type, TypeExpr::Effect(_, _));
    let ret = if is_async {
        match &f.type_sig.return_type {
            TypeExpr::Effect(_, inner) => format!("Promise<{}>", type_to_ts(inner)),
            _ => "Promise<any>".into(),
        }
    } else {
        type_to_ts(&f.type_sig.return_type)
    };
    let async_kw = if is_async { "async " } else { "" };
    out.push_str(&format!("  export {}function {}({}): {} {{\n", async_kw, f.name, params.join(", "), ret));
    // require: → if (!(cond)) throw
    for req in &f.requires {
        out.push_str(&format!("    if (!({})) throw new Error(\"require violation: {}\");\n", req.expr, req.expr));
    }
    // Body
    if !f.body.is_empty() {
        let body_str = f.body.join(" ");
        // HOF sugar: map(xs, fn) → xs.map(fn)
        if body_str.trim_start().starts_with("map (") || body_str.trim_start().starts_with("map(") {
            out.push_str(&format!("    return ({}).map(x => x);\n", body_str));
        } else {
            out.push_str(&format!("    return {} as any;\n", body_str));
        }
    } else {
        out.push_str("    return undefined as any;\n");
    }
    // ensure: → capture result and check
    if !f.ensures.is_empty() {
        out.push_str("    const _loomResult = undefined as any;\n");
        for ens in &f.ensures {
            out.push_str(&format!("    if (!(_loomResult !== undefined && ({}))) throw new Error(\"ensure violation: {}\");\n", ens.expr, ens.expr));
        }
    }
    out.push_str("  }\n\n");
}

fn emit_ts_fn_method(f: &FnDef, out: &mut String) {
    let params: Vec<String> = f.type_sig.params.iter().enumerate()
        .map(|(i, ty)| format!("p{}: {}", i, type_to_ts(ty)))
        .collect();
    let ret = type_to_ts(&f.type_sig.return_type);
    out.push_str(&format!("    {}({}): {} {{\n", f.name, params.join(", "), ret));
    out.push_str("      return undefined as any;\n");
    out.push_str("    }\n");
}

fn emit_ts_being(being: &BeingDef, out: &mut String) {
    if let Some(desc) = &being.describe {
        out.push_str(&format!("/** {} */\n", desc));
    }
    if let Some(telos) = &being.telos {
        out.push_str(&format!("/** @telos {} */\n", telos.description));
    }
    out.push_str(&format!("export class {} {{\n", being.name));

    // matter fields as class properties
    if let Some(matter) = &being.matter {
        for field in &matter.fields {
            let ts_ty = type_to_ts(&field.ty);
            for ann in &field.annotations {
                out.push_str(&format!("  /** @{} */\n", ann.key));
            }
            out.push_str(&format!("  {}!: {};\n", field.name, ts_ty));
        }
    }

    // telomere counter
    if let Some(tel) = &being.telomere {
        out.push_str(&format!("  readonly telomereLimit: number = {};\n", tel.limit));
        out.push_str("  private _telomereCount: number = 0;\n");
    }
    out.push('\n');

    // constructor
    out.push_str("  constructor(init?: Partial<{}>) {{\n".replace("{}", &being.name).as_str());
    out.push_str("    if (init) Object.assign(this, init);\n  }\n\n");

    // fitness()
    if being.telos.is_some() {
        out.push_str("  fitness(): number { throw new Error('not implemented'); }\n\n");
    }

    // regulate()
    for reg in &being.regulate_blocks {
        out.push_str(&format!(
            "  regulate{}(): void {{ throw new Error('not implemented'); }}\n\n",
            pascal_case(&reg.variable)
        ));
    }

    // evolve — per-strategy methods + dispatcher
    if let Some(evolve) = &being.evolve_block {
        for case in &evolve.search_cases {
            let strategy = match case.strategy {
                SearchStrategy::GradientDescent => "GradientDescent",
                SearchStrategy::StochasticGradient => "StochasticGradient",
                SearchStrategy::SimulatedAnnealing => "SimulatedAnnealing",
                SearchStrategy::DerivativeFree => "DerivativeFree",
                SearchStrategy::Mcmc => "Mcmc",
            };
            out.push_str(&format!(
                "  evolve{}(): void {{ throw new Error('not implemented'); }}\n\n",
                strategy
            ));
        }
        // Dispatcher with convergence loop
        out.push_str("  evolveStep(): void {\n");
        out.push_str("    let distance = this.fitness();\n");
        out.push_str("    while (distance > 0) {\n");
        for case in &evolve.search_cases {
            let strategy = match case.strategy {
                SearchStrategy::GradientDescent => "GradientDescent",
                SearchStrategy::StochasticGradient => "StochasticGradient",
                SearchStrategy::SimulatedAnnealing => "SimulatedAnnealing",
                SearchStrategy::DerivativeFree => "DerivativeFree",
                SearchStrategy::Mcmc => "Mcmc",
            };
            out.push_str(&format!("      this.evolve{}();\n", strategy));
        }
        out.push_str("      distance = this.fitness();\n");
        out.push_str("    }\n");
        out.push_str("  }\n\n");

        // Keep legacy evolve()
        out.push_str("  evolve(): void { this.evolveStep(); }\n\n");
    }

    // replicate() with telomere limit
    if let Some(tel) = &being.telomere {
        out.push_str(&format!(
            "  replicate(): {} | null {{\n",
            being.name
        ));
        out.push_str(&format!(
            "    if (this._telomereCount >= {}) return null;\n",
            tel.limit
        ));
        out.push_str("    this._telomereCount++;\n");
        out.push_str(&format!("    return new {}(this as any);\n  }}\n\n", being.name));
    }

    // autopoietic
    if being.autopoietic {
        out.push_str("  static isAutopoietic(): boolean { return true; }\n");
        out.push_str("  verifyClosure(): boolean { throw new Error('not implemented'); }\n\n");
    }

    // function: block methods
    if let Some(func) = &being.function {
        for f in &func.fns {
            let params: Vec<String> = f
                .type_sig
                .params
                .iter()
                .enumerate()
                .map(|(i, ty)| format!("p{}: {}", i, type_to_ts(ty)))
                .collect();
            let ret = type_to_ts(&f.type_sig.return_type);
            out.push_str(&format!(
                "  {}({}): {} {{ throw new Error('not implemented'); }}\n\n",
                f.name,
                params.join(", "),
                ret
            ));
        }
    }

    // epigenetic methods
    for epi in &being.epigenetic_blocks {
        let method_name = format!("applyEpigenetic{}", pascal_case(&epi.signal));
        out.push_str(&format!("  {}(signalStrength: number): void {{ throw new Error('not implemented'); }}\n\n", method_name));
    }

    // morphogen methods
    for morph in &being.morphogen_blocks {
        let method_name = format!("differentiate{}", pascal_case(&morph.signal));
        out.push_str(&format!("  {}(signalLevel: number): void {{ throw new Error('not implemented'); }}\n\n", method_name));
    }

    out.push_str("}\n\n");
}

fn emit_ts_ecosystem(eco: &EcosystemDef, out: &mut String) {
    if let Some(telos) = &eco.telos {
        out.push_str(&format!("/** @telos {} */\n", telos));
    }
    out.push_str(&format!("export namespace {} {{\n", eco.name));

    for sig in &eco.signals {
        out.push_str(&format!(
            "  export interface {}Signal {{ from: string; to: string; }}\n",
            sig.name
        ));
    }

    out.push_str("  export function coordinate(): void { throw new Error('not implemented'); }\n");
    out.push_str("}\n\n");
}

// ── Type rendering helpers ────────────────────────────────────────────────────

fn type_to_ts(ty: &TypeExpr) -> String {
    match ty {
        TypeExpr::Base(n) => match n.as_str() {
            "Int" => "number".into(),
            "Float" => "number".into(),
            "String" => "string".into(),
            "Bool" => "boolean".into(),
            "Unit" => "void".into(),
            other => other.to_string(),
        },
        TypeExpr::Generic(n, args) => match n.as_str() {
            "List" => format!("{}[]", args.iter().map(type_to_ts).collect::<Vec<_>>().join(", ")),
            "Option" => format!("{} | null", args.iter().map(type_to_ts).collect::<Vec<_>>().join(", ")),
            "Result" => format!("{{ ok: {} }} | {{ err: {} }}", type_to_ts(&args[0]), type_to_ts(args.get(1).unwrap_or(&TypeExpr::Base("unknown".into())))),
            "Float" if args.len() == 1 => {
                if let TypeExpr::Base(unit) = &args[0] {
                    format!("number & {{ readonly _unit: \"{}\" }}", unit)
                } else {
                    "number".into()
                }
            }
            _ => format!("{}<{}>", n, args.iter().map(type_to_ts).collect::<Vec<_>>().join(", ")),
        },
        TypeExpr::Effect(_, ret) => format!("Promise<{}>", type_to_ts(ret)),
        TypeExpr::Option(inner) => format!("{} | null", type_to_ts(inner)),
        TypeExpr::Result(ok, err) => format!("{{ ok: {} }} | {{ err: {} }}", type_to_ts(ok), type_to_ts(err)),
        TypeExpr::Tuple(types) => format!("[{}]", types.iter().map(type_to_ts).collect::<Vec<_>>().join(", ")),
        TypeExpr::Fn(a, b) => format!("(p: {}) => {}", type_to_ts(a), type_to_ts(b)),
        TypeExpr::TypeVar(n) => format!("T{}", n),
    }
}

fn type_to_zod(ty: &TypeExpr) -> String {
    match ty {
        TypeExpr::Base(n) => match n.as_str() {
            "Int" | "Float" => "z.number()".into(),
            "String" => "z.string()".into(),
            "Bool" => "z.boolean()".into(),
            "Unit" => "z.undefined()".into(),
            _ => format!("z.unknown() /* {} */", n),
        },
        TypeExpr::Generic(n, args) => match n.as_str() {
            "List" => format!("z.array({})", type_to_zod(&args[0])),
            "Option" => format!("{}.optional()", type_to_zod(&args[0])),
            "Float" => "z.number()".into(),
            _ => "z.unknown()".into(),
        },
        TypeExpr::Option(inner) => format!("{}.optional()", type_to_zod(inner)),
        _ => "z.unknown()".into(),
    }
}

fn capitalize(s: &str) -> String {
    let mut chars = s.chars();
    match chars.next() {
        None => String::new(),
        Some(c) => c.to_uppercase().collect::<String>() + chars.as_str(),
    }
}

fn pascal_case(s: &str) -> String {
    s.split('_').map(|w| {
        let mut c = w.chars();
        match c.next() {
            None => String::new(),
            Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
        }
    }).collect()
}
