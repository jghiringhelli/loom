// ALX: derived from loom.loom §"emit_typescript" and language-spec.md §9
// TypeDef → export interface + Zod schema
// EnumDef → export enum
// FnDef → export function with JSDoc
// BeingDef → export class with TS equivalents of Rust methods
// EcosystemDef → export namespace with signal types

use crate::ast::*;

/// G3: TypeScriptEmitter struct — tests call `TypeScriptEmitter::new().emit(&module)`.
pub struct TypeScriptEmitter;

impl TypeScriptEmitter {
    pub fn new() -> Self { TypeScriptEmitter }
    pub fn emit(&self, module: &Module) -> String {
        emit_typescript(module)
    }
}

pub fn emit_typescript(module: &Module) -> String {
    let mut out = String::new();

    // Header
    out.push_str("// Generated by Loom ALX compiler.\n");
    out.push_str("// @ts-nocheck\n\n");
    out.push_str("import { z } from 'zod';\n\n");

    // Flow labels → branded types (language-spec.md §9.5)
    for fl in &module.flow_labels {
        for ty_name in &fl.types {
            out.push_str(&format!(
                "export type {} = string & {{ readonly _sensitivity: \"{}\" }};\n",
                ty_name, fl.label
            ));
        }
    }
    if !module.flow_labels.is_empty() {
        out.push('\n');
    }

    // Lifecycle → state union type (language-spec.md §9.4)
    for lc in &module.lifecycle_defs {
        out.push_str(&format!(
            "export type {}State = {};\n",
            lc.type_name,
            lc.states.iter().map(|s| format!("\"{}\"", s)).collect::<Vec<_>>().join(" | ")
        ));
    }
    if !module.lifecycle_defs.is_empty() {
        out.push('\n');
    }

    // Items
    for item in &module.items {
        match item {
            Item::Type(t) => emit_ts_type(t, &mut out),
            Item::Enum(e) => emit_ts_enum(e, &mut out),
            Item::RefinedType(r) => emit_ts_refined(r, &mut out),
            Item::Fn(f) => emit_ts_fn(f, &mut out),
        }
    }

    // Beings → export class
    for being in &module.being_defs {
        emit_ts_being(being, &mut out);
    }

    // Ecosystems → export namespace
    for eco in &module.ecosystem_defs {
        emit_ts_ecosystem(eco, &mut out);
    }

    out
}

fn emit_ts_type(t: &TypeDef, out: &mut String) {
    out.push_str(&format!("export interface {} {{\n", t.name));
    for field in &t.fields {
        let ts_ty = type_to_ts(&field.ty);
        // Privacy annotations as JSDoc
        for ann in &field.annotations {
            out.push_str(&format!("  /** @{} */\n", ann.key));
        }
        out.push_str(&format!("  {}: {};\n", field.name, ts_ty));
    }
    out.push_str("}\n\n");

    // Zod schema
    out.push_str(&format!("export const {}Schema = z.object({{\n", t.name));
    for field in &t.fields {
        let zod = type_to_zod(&field.ty);
        out.push_str(&format!("  {}: {},\n", field.name, zod));
    }
    out.push_str("});\n\n");
}

fn emit_ts_enum(e: &EnumDef, out: &mut String) {
    out.push_str(&format!("export enum {} {{\n", e.name));
    for v in &e.variants {
        out.push_str(&format!("  {} = \"{}\",\n", v.name, v.name));
    }
    out.push_str("}\n\n");
}

fn emit_ts_refined(r: &RefinedType, out: &mut String) {
    let base = type_to_ts(&r.base_type);
    out.push_str(&format!("// Refined type: {}\n", r.name));
    out.push_str(&format!("// predicate: {}\n", r.predicate));
    out.push_str(&format!("export type {} = {};\n\n", r.name, base));
}

fn emit_ts_fn(f: &FnDef, out: &mut String) {
    if let Some(desc) = &f.describe {
        out.push_str(&format!("/** {} */\n", desc));
    }
    for ann in &f.annotations {
        out.push_str(&format!("/** @{} */\n", ann.key));
    }
    let params: Vec<String> = f
        .type_sig
        .params
        .iter()
        .enumerate()
        .map(|(i, ty)| format!("p{}: {}", i, type_to_ts(ty)))
        .collect();
    let ret = type_to_ts(&f.type_sig.return_type);
    out.push_str(&format!(
        "export function {}({}): {} {{\n  throw new Error('not implemented');\n}}\n\n",
        f.name,
        params.join(", "),
        ret
    ));
}

fn emit_ts_being(being: &BeingDef, out: &mut String) {
    if let Some(desc) = &being.describe {
        out.push_str(&format!("/** {} */\n", desc));
    }
    if let Some(telos) = &being.telos {
        out.push_str(&format!("/** @telos {} */\n", telos.description));
    }
    out.push_str(&format!("export class {} {{\n", being.name));

    // matter fields as class properties
    if let Some(matter) = &being.matter {
        for field in &matter.fields {
            let ts_ty = type_to_ts(&field.ty);
            for ann in &field.annotations {
                out.push_str(&format!("  /** @{} */\n", ann.key));
            }
            out.push_str(&format!("  {}!: {};\n", field.name, ts_ty));
        }
    }

    // telomere counter
    if being.telomere.is_some() {
        out.push_str("  private _telomereCount: number = 0;\n");
    }
    out.push('\n');

    // constructor
    out.push_str("  constructor(init?: Partial<{}>) {{\n".replace("{}", &being.name).as_str());
    out.push_str("    if (init) Object.assign(this, init);\n  }\n\n");

    // fitness()
    if being.telos.is_some() {
        out.push_str("  fitness(): number { throw new Error('not implemented'); }\n\n");
    }

    // regulate()
    for reg in &being.regulate_blocks {
        out.push_str(&format!(
            "  regulate{}(): void {{ throw new Error('not implemented'); }}\n\n",
            pascal_case(&reg.variable)
        ));
    }

    // evolve()
    if being.evolve_block.is_some() {
        out.push_str("  evolve(): void { throw new Error('not implemented'); }\n\n");
    }

    // replicate() with telomere limit
    if let Some(tel) = &being.telomere {
        out.push_str(&format!(
            "  replicate(): {} | null {{\n",
            being.name
        ));
        out.push_str(&format!(
            "    if (this._telomereCount >= {}) return null;\n",
            tel.limit
        ));
        out.push_str("    this._telomereCount++;\n");
        out.push_str(&format!("    return new {}(this as any);\n  }}\n\n", being.name));
    }

    // autopoietic
    if being.autopoietic {
        out.push_str("  static isAutopoietic(): boolean { return true; }\n");
        out.push_str("  verifyClosure(): boolean { throw new Error('not implemented'); }\n\n");
    }

    // function: block methods
    if let Some(func) = &being.function {
        for f in &func.fns {
            let params: Vec<String> = f
                .type_sig
                .params
                .iter()
                .enumerate()
                .map(|(i, ty)| format!("p{}: {}", i, type_to_ts(ty)))
                .collect();
            let ret = type_to_ts(&f.type_sig.return_type);
            out.push_str(&format!(
                "  {}({}): {} {{ throw new Error('not implemented'); }}\n\n",
                f.name,
                params.join(", "),
                ret
            ));
        }
    }

    out.push_str("}\n\n");
}

fn emit_ts_ecosystem(eco: &EcosystemDef, out: &mut String) {
    if let Some(telos) = &eco.telos {
        out.push_str(&format!("/** @telos {} */\n", telos));
    }
    out.push_str(&format!("export namespace {} {{\n", eco.name));

    for sig in &eco.signals {
        out.push_str(&format!(
            "  export interface {}Signal {{ from: string; to: string; }}\n",
            sig.name
        ));
    }

    out.push_str("  export function coordinate(): void { throw new Error('not implemented'); }\n");
    out.push_str("}\n\n");
}

// ── Type rendering helpers ────────────────────────────────────────────────────

fn type_to_ts(ty: &TypeExpr) -> String {
    match ty {
        TypeExpr::Base(n) => match n.as_str() {
            "Int" => "number".into(),
            "Float" => "number".into(),
            "String" => "string".into(),
            "Bool" => "boolean".into(),
            "Unit" => "void".into(),
            other => other.to_string(),
        },
        TypeExpr::Generic(n, args) => match n.as_str() {
            "List" => format!("{}[]", args.iter().map(type_to_ts).collect::<Vec<_>>().join(", ")),
            "Option" => format!("{} | null", args.iter().map(type_to_ts).collect::<Vec<_>>().join(", ")),
            "Result" => format!("{{ ok: {} }} | {{ err: {} }}", type_to_ts(&args[0]), type_to_ts(args.get(1).unwrap_or(&TypeExpr::Base("unknown".into())))),
            "Float" if args.len() == 1 => {
                if let TypeExpr::Base(unit) = &args[0] {
                    format!("number & {{ readonly _unit: \"{}\" }}", unit)
                } else {
                    "number".into()
                }
            }
            _ => format!("{}<{}>", n, args.iter().map(type_to_ts).collect::<Vec<_>>().join(", ")),
        },
        TypeExpr::Effect(_, ret) => format!("Promise<{}>", type_to_ts(ret)),
        TypeExpr::Option(inner) => format!("{} | null", type_to_ts(inner)),
        TypeExpr::Result(ok, err) => format!("{{ ok: {} }} | {{ err: {} }}", type_to_ts(ok), type_to_ts(err)),
        TypeExpr::Tuple(types) => format!("[{}]", types.iter().map(type_to_ts).collect::<Vec<_>>().join(", ")),
        TypeExpr::Fn(a, b) => format!("(p: {}) => {}", type_to_ts(a), type_to_ts(b)),
        TypeExpr::TypeVar(n) => format!("T{}", n),
    }
}

fn type_to_zod(ty: &TypeExpr) -> String {
    match ty {
        TypeExpr::Base(n) => match n.as_str() {
            "Int" | "Float" => "z.number()".into(),
            "String" => "z.string()".into(),
            "Bool" => "z.boolean()".into(),
            "Unit" => "z.undefined()".into(),
            _ => format!("z.unknown() /* {} */", n),
        },
        TypeExpr::Generic(n, args) => match n.as_str() {
            "List" => format!("z.array({})", type_to_zod(&args[0])),
            "Option" => format!("{}.optional()", type_to_zod(&args[0])),
            "Float" => "z.number()".into(),
            _ => "z.unknown()".into(),
        },
        TypeExpr::Option(inner) => format!("{}.optional()", type_to_zod(inner)),
        _ => "z.unknown()".into(),
    }
}

fn pascal_case(s: &str) -> String {
    s.split('_').map(|w| {
        let mut c = w.chars();
        match c.next() {
            None => String::new(),
            Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
        }
    }).collect()
}
