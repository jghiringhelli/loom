// ALX: derived from loom.loom §"emit_simulation" (M52)
// Mesa ABM Python simulation emitter — Maturana/Varela autopoietic beings.
// - Only beings with autopoietic: true get full agent treatment
// - Being → Python class inheriting mesa.Agent
// - Ecosystem → Python class inheriting mesa.Model
// - regulate: → homeostasis check in step()
// - telomere: → replication limit enforced in step()

use crate::ast::*;

/// G3: SimulationEmitter struct — tests call `SimulationEmitter::new().emit(&module)`.
pub struct SimulationEmitter;

impl SimulationEmitter {
    pub fn new() -> Self { SimulationEmitter }
    pub fn emit(&self, module: &Module) -> String {
        emit_simulation(module)
    }
}

pub fn emit_simulation(module: &Module) -> String {
    let mut out = String::new();

    out.push_str("# Generated by Loom ALX compiler — Mesa ABM simulation\n");
    out.push_str("# M52: Maturana/Varela autopoietic beings\n\n");
    out.push_str("import mesa\n");
    out.push_str("from mesa.time import RandomActivation\n\n");

    // Emit autopoietic beings as Mesa agents
    for being in &module.being_defs {
        if being.autopoietic {
            emit_mesa_agent(&mut out, being);
        }
    }

    // Emit ecosystems as Mesa models
    for eco in &module.ecosystem_defs {
        emit_mesa_model(&mut out, eco, &module.being_defs);
    }

    out
}

fn emit_mesa_agent(out: &mut String, being: &BeingDef) {
    if let Some(desc) = &being.describe {
        out.push_str(&format!("# {}\n", desc));
    }
    if let Some(telos) = &being.telos {
        out.push_str(&format!("# telos: {}\n", telos.description));
    }
    out.push_str(&format!("class {}(mesa.Agent):\n", being.name));
    out.push_str("    \"\"\"Autopoietic being — self-producing Mesa agent.\"\"\"\n\n");

    // __init__
    out.push_str("    def __init__(self, unique_id, model):\n");
    out.push_str("        super().__init__(unique_id, model)\n");

    // matter fields as instance variables
    if let Some(matter) = &being.matter {
        for field in &matter.fields {
            let default = python_default(&field.ty);
            out.push_str(&format!("        self.{} = {}\n", field.name, default));
        }
    }

    // telomere counter
    if let Some(tel) = &being.telomere {
        out.push_str(&format!("        self._replication_count = 0\n"));
        out.push_str(&format!("        self._telomere_limit = {}\n", tel.limit));
    }

    out.push('\n');

    // step()
    out.push_str("    def step(self):\n");
    out.push_str("        \"\"\"Execute one simulation step.\"\"\"\n");

    // regulate: → homeostasis check
    for reg in &being.regulate_blocks {
        if let Some((lo, hi)) = &reg.bounds {
            out.push_str(&format!(
                "        # homeostasis: {} in [{}, {}]\n",
                reg.variable, lo, hi
            ));
            out.push_str(&format!(
                "        if hasattr(self, '{}'):\n",
                reg.variable
            ));
            out.push_str(&format!(
                "            if not ({} <= self.{} <= {}):\n",
                lo, reg.variable, hi
            ));
            out.push_str("                self._regulate()\n");
        }
    }

    // evolve: → directed search
    if let Some(evolve) = &being.evolve_block {
        out.push_str(&format!("        # evolve: constraint={}\n", evolve.constraint));
        out.push_str("        self.evolve()\n");
    }

    // telomere: → replication limit
    if let Some(tel) = &being.telomere {
        out.push_str(&format!(
            "        if self._replication_count >= {}:\n",
            tel.limit
        ));
        out.push_str(&format!("            self.{}()\n", tel.on_exhaustion));
        out.push_str("            return\n");
    }

    out.push_str("        pass  # implement step logic\n\n");

    // regulate method
    if !being.regulate_blocks.is_empty() {
        out.push_str("    def _regulate(self):\n");
        out.push_str("        \"\"\"Homeostatic regulation response.\"\"\"\n");
        for reg in &being.regulate_blocks {
            for resp in &reg.response {
                out.push_str(&format!("        # response: {}\n", resp));
            }
        }
        out.push_str("        pass\n\n");
    }

    // evolve method
    if let Some(evolve) = &being.evolve_block {
        out.push_str("    def evolve(self):\n");
        out.push_str(&format!("        \"\"\"Directed search: {}\"\"\"\n", evolve.constraint));
        for case in &evolve.search_cases {
            let strategy = match case.strategy {
                SearchStrategy::GradientDescent => "gradient_descent",
                SearchStrategy::StochasticGradient => "stochastic_gradient",
                SearchStrategy::SimulatedAnnealing => "simulated_annealing",
                SearchStrategy::DerivativeFree => "derivative_free",
                SearchStrategy::Mcmc => "mcmc",
            };
            out.push_str(&format!("        # {}: when {}\n", strategy, case.when));
        }
        out.push_str("        pass\n\n");
    }

    // replicate method (telomere)
    if let Some(tel) = &being.telomere {
        out.push_str("    def replicate(self):\n");
        out.push_str("        \"\"\"Replicate with Hayflick limit.\"\"\"\n");
        out.push_str(&format!(
            "        if self._replication_count >= {}:\n",
            tel.limit
        ));
        out.push_str("            return None\n");
        out.push_str("        self._replication_count += 1\n");
        out.push_str(&format!(
            "        child = {}(self.model.next_id(), self.model)\n",
            being.name
        ));
        out.push_str("        self.model.schedule.add(child)\n");
        out.push_str("        return child\n\n");

        // on_exhaustion stub
        out.push_str(&format!("    def {}(self):\n", tel.on_exhaustion));
        out.push_str("        \"\"\"Called when telomere limit reached.\"\"\"\n");
        out.push_str("        self.model.schedule.remove(self)\n\n");
    }
}

fn emit_mesa_model(out: &mut String, eco: &EcosystemDef, beings: &[BeingDef]) {
    if let Some(telos) = &eco.telos {
        out.push_str(&format!("# telos: {}\n", telos));
    }
    out.push_str(&format!("class {}(mesa.Model):\n", eco.name));
    out.push_str("    \"\"\"Ecosystem Mesa model.\"\"\"\n\n");

    out.push_str("    def __init__(self):\n");
    out.push_str("        super().__init__()\n");
    out.push_str("        self.schedule = RandomActivation(self)\n");

    // Add member beings
    for member in &eco.members {
        let being = beings.iter().find(|b| b.name == *member);
        if being.map(|b| b.autopoietic).unwrap_or(false) {
            out.push_str(&format!(
                "        agent = {}(self.next_id(), self)\n",
                member
            ));
            out.push_str("        self.schedule.add(agent)\n");
        }
    }
    out.push('\n');

    // step()
    out.push_str("    def step(self):\n");
    out.push_str("        \"\"\"Advance all agents by one step.\"\"\"\n");

    // quorum check
    if let Some(quorum) = eco.quorum_blocks.first() {
        out.push_str(&format!(
            "        # quorum: threshold={} for signal '{}'\n",
            quorum.threshold,
            quorum.signal
        ));
    }

    out.push_str("        self.schedule.step()\n\n");
}

fn python_default(ty: &TypeExpr) -> String {
    match ty {
        TypeExpr::Base(n) => match n.as_str() {
            "Int" => "0".into(),
            "Float" => "0.0".into(),
            "String" => "\"\"".into(),
            "Bool" => "False".into(),
            _ => "None".into(),
        },
        TypeExpr::Generic(n, _) => match n.as_str() {
            "List" => "[]".into(),
            "Option" => "None".into(),
            "Float" => "0.0".into(),
            _ => "None".into(),
        },
        _ => "None".into(),
    }
}
